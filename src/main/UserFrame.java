    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        String num0 = fieldNum0.getText();
        String num1 = fieldNum1.getText();
        String num2 = fieldNum2.getText();
        String num3 = fieldNum3.getText();
        String num4 = fieldNum4.getText();
     
        if(num0.isEmpty() || num1.isEmpty() || num2.isEmpty() || num3.isEmpty() || num4.isEmpty()){
            JOptionPane.showMessageDialog(null, "All Field Cannot Null", "Notification", JOptionPane.INFORMATION_MESSAGE);
            return;
        }else if(num0.equals("0") || num1.equals("0") || num2.equals("0") || num3.equals("0") || num4.equals("0")){
            JOptionPane.showMessageDialog(null, "Cannot input zero (0)", "Notification", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        int[] nums = {
            Integer.valueOf(num0),
            Integer.valueOf(num1),
            Integer.valueOf(num2),
            Integer.valueOf(num3),
            Integer.valueOf(num4),
        };
         
        //call the sort function with parameter random numbers
        this.selectionSort(this.numbers);
        
        //check if sorted numbers from user is equal to sorted numbers from sort function
        //using Arrays.equals() function
        
        //if true will display correct message and point +5
        //if false will display wrong message and clear the textfield
        if(!Arrays.equals(nums, this.numbers)){
            JOptionPane.showMessageDialog(null, "Wrong! Try Again");
            this.clear();
            return;
        }
        
        JOptionPane.showMessageDialog(null, "Correct!");
        this.point += 5;
        
        for (int i = 0; i < this.numbers.length; i++) {
            this.numbers[i] = 0;
        }
        
        clear();
        random();
        setText();
    }//GEN-LAST:event_btnSubmitActionPerformed

