    public void random(){
        int min = 0;
        int max = 1000;
        int random;
        
        for (int i = 0; i < this.numbers.length; i++) {
            random = (int)Math.floor(Math.random()*(max-min+1)+min);
            this.numbers[i] = random;
        }        
    }
    
