    //function to sort the random numbers
    //using selection sort algorithm
    public void selectionSort(int[] numbers){
        //put the array length to variabel
        int length = numbers.length;
        
        //loop the unsorted array
        for (int i = 0; i < length - 1; i++){    
            
            //find the minimum element(index) in unsorted array
            int minIndex = i;  
            for (int j = i + 1; j < length; j++){  
                if (numbers[j] < numbers[minIndex]){  
                    minIndex = j;
                }  
            } 
            
            //swap the found minimum element(index) with the first element(index)
            int temp = numbers[minIndex];   
            numbers[minIndex] = numbers[i];  
            numbers[i] = temp;  
        }  
    }
    
    class setTimer extends TimerTask {
        int second = Integer.parseInt(labelTimer.getText());
     
        public void run() {
            if (second > 0){
                labelTimer.setText(String.valueOf(second));
                second--;
            }else{
                labelTimer.setText("0");
            }
        }
    }
    
    public void timer(){
        timer = new Timer();
        timer.schedule(new setTimer(),0,1000);
    }
    
    public void startTimer(){
        labelTimer.setText("60");        
        setText();
        this.timer();
    }

